#include "libft.h"

size_t		ft_nbrlen(int nb)
{
	size_t cp;

	if (nb == 0)
		return (1);
	if (nb < 0)
		nb = nb * (-1);
	if (nb == -2147483648)
		return (10);
	if (nb > 2147483647)
		return (0);
	cp = 0;
	while (nb > 0)
	{
		cp++;
		nb = (nb / 10);
	}
	return (cp);
}
